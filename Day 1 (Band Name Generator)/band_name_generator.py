# A small band name generator 
# This program uses user input to combine two inputs into a single string 

# Print program name
print('Welcome to a band name generator')
# ask user for city name and save in city_name
city_name = input('Please enter the name of the city you were born in below: \n')
# ask user for pet name and save in pet_name
pet_name = input('Please enter the name of a childhood pet you owned: \n')
# join sting using concatenation 
print('Your new band name maybe: ' + city_name + " " + pet_name)
